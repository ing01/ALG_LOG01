##Faça um programa que receba o número de horas trabalhadas e o valor do salário mínimo, calcule e mostre o salário a receber, seguindo estas regras:
##a hora trabalhada vale a metade do salário mínimo; o salário bruto equivale ao número de horas trabalhadas multiplicado pelo valor da hora trabalhada.
##o imposto equivale a 3% do salário bruto; o salário a receber equivale ao salário bruto menos o imposto.

horas_trab = int(input('Quantas horas você trabalha? '))
salario_min = float(input('Qual o salário mínimo? '))
valor_hr = salario_min /2
valor_sal_brut = valor_hr * horas_trab
imposto = valor_sal_brut * 3/100
valor_sal_liq = valor_sal_brut - imposto
print('O salário recebido é R$',valor_sal_liq)
